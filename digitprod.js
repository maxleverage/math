var strsplit = function(n) {
	var output = [];
	var split = [];
	for(ii in n) {
		n[ii] += '';
		split = n[ii].split("");
		for(jj in split) {
			output.push(split[jj]);
		}
	}
	return output;
};


// Product Function

var product = function(as) {
	var output = [];
	var multiple = 0;
	for(ii=0; ii <= (as.length - 5); ii++) {
		multiple = as[ii] * as[ii+1] * as[ii+2] * as[ii+3] * as[ii+4];
		output.push(multiple);
	}
	return output;
};

